public class For05 {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("\nTabuada de "+i+"\n");
            for (int j = 0; j <= 10; j++) {
                System.out.println(i+ "*"+ j+ "="+(i*j));
            }
            }
        }
}

